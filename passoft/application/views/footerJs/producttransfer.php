          // alert(reciadmin'+'recivebranch);
         // alert(data);
           $("#branchproduct1").show();
          $("#branchproduct1").html(data);
          

        });

      }

    });
  }
  if (cat == 3) {
    $("#stud").hide();
    $("#adminproduct").hide();
    $("#branchproduct1").hide();
    $("#emp").show();
    $("#sub_branch").change(function() {

      var subbranchid = $("#sub_branch").val();

            //alert('3');
            alert("please select Subbranch");
            $("#branch").get(0).selectedIndex = 0;


          }
        }

        $("#subbranchproduct").hide();
      } else {

        $.post("<?php echo site_url('branchController/shopproduct')?>", {
          subbranchid: subbranchid,
          reciadmin: reciadmin,
          recivebranch: recivebranch,
          recisubbranch: recisubbranch
